/// Extension trait that constrains the `RCC` peripheral
pub trait RccExt {
    /// Constrains the `RCC` peripheral so it plays nicely with the
    /// other abstractions
    fn constrain(self) -> Rcc;
}

impl RccExt for RCC {
    fn constrain(self) -> Rcc {
        Rcc {
            config: Config {
                hse: None,
                sys_ck: None,
                per_ck: None,
                rcc_hclk: None,
                rcc_pclk1: None,
                rcc_pclk2: None,
                rcc_pclk3: None,
                rcc_pclk4: None,
                pll1: PllConfig {
                    strategy: PllConfigStrategy::Normal,
                    p_ck: None,
                    q_ck: None,
                    r_ck: None,
                },
                pll2: PllConfig {
                    strategy: PllConfigStrategy::Normal,
                    p_ck: None,
                    q_ck: None,
                    r_ck: None,
                },
                pll3: PllConfig {
                    strategy: PllConfigStrategy::Normal,
                    p_ck: None,
                    q_ck: None,
                    r_ck: None,
                },
            },
            rb: self,
        }
    }
}

/// Constrained RCC peripheral
///
/// Generated by calling `constrain` on the PAC's RCC peripheral.
///
/// ```rust
